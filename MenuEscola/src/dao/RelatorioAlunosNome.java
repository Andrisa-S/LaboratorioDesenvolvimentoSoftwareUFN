    public void preencheTabela(){
        AlunoDAO aDAO = new AlunoDAO();
        List<Aluno> listaAlunos = aDAO.getAlunosNome(txtNome.getText());
        
        DefaultTableModel tabelaAlunos = (DefaultTableModel) tblPessoas.getModel();
        
        tabelaAlunos.setNumRows(0);
        
        //Percorre a lista de pessoas que foi consultada e adiciona na tabela
        for(Aluno a: listaAlunos){
            Object[] obj = new Object[]{
                a.getId(),
                a.getNome(),
                a.getIdade(),
                a.getCurso()};
             tabelaAlunos.addRow(obj);
        }
    }
    
