    public void preencheTabela(){
        String sexo = "";
        PessoaDAO pDAO = new PessoaDAO();
        
        if (rdoFeminino.isSelected()){
            sexo = "F";
        } else if (rdoMasculino.isSelected()){
            sexo = "M";
        } else
            sexo = "";
        List<Pessoa> listaPessoas = pDAO.getPessoasNome(txtNome.getText(), sexo);
        
        DefaultTableModel tabelaPessoas = (DefaultTableModel) tblPessoas.getModel();
        
        tabelaPessoas.setNumRows(0);
        
        //Percorre a lista de pessoas que foi consultada e adiciona na tabela
        for(Pessoa p: listaPessoas){
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getSexo(),
                p.getIdioma()};
             tabelaPessoas.addRow(obj);
        }
    }
    
